import std;
using namespace std;

int main() {
  // valid names
  int x;
  int number_of_elements;
  int Fourier_transform;
  int z2;
  int Polygon;

  // not names
  // int 2x;             // a name must start with a letter
  // int time@to@market; // @ is not a letter, digit, or underscore
  // int Start menu;     // space is not a letter, digit, or underscore

  // don't use reserved names such as keywords to name your variables, types, functions, etc
  // int if = 7;     // error: if is a keyword
  // int string = 7; // this will lead to trouble

  // choose meaningful names
  // abbreviations and acronyms can be confusing, use them sparingly
  int mtbf;
  int TLA;
  int myw;
  int NBV;

  // short names are meaningful when used conventionally
  // int x; // local variable or parameter
  int i;    // loop index

  // OK names
  int partial_sum;
  int element_count;
  int stable_partition;

  // too long
  int the_number_of_elements;

  // avoid names that are easy to mistype, misread, or confuse
  int Name;
  int names;
  int nameS;
  int foo;
  int f00;
  int fl;
  int f1;
  int fI;
  int fi;
}
